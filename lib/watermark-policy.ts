import { getPlan } from '@/lib/entitlements';

export type WatermarkContext = {
  planId: string | null;
  isTrialing?: boolean;
};

export function shouldDisplayWatermark({ planId, isTrialing = false }: WatermarkContext): boolean {
  if (!planId) {
    return true;
  }

  const plan = getPlan(planId);
  if (!plan) {
    return true;
  }

  // Free plan has watermark
  if (plan.id === 'free') {
    return true;
  }

  return isTrialing;
}

